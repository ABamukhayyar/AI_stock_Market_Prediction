const LanguageContext = createContext({
  lang: 'en',
  isRTL: false,
  t: (key, params) => {
    if (!params) return key;
    return Object.entries(params).reduce((value, [paramKey, paramValue]) => {
      return value.replace(new RegExp(`\\{${paramKey}\\}`, 'g'), String(paramValue));
    }, key);
  },
  setLanguage: () => {},
  toggleLang: () => {},
});

function formatMessage(message, params) {
  if (!params) return message;

  return Object.entries(params).reduce((value, [key, replacement]) => {
    return value.replace(new RegExp(`\\{${key}\\}`, 'g'), String(replacement));
  }, message);
}

export function LanguageProvider({ children }) {
  const [lang, setLang] = useState(() => {
    if (typeof window === 'undefined') return 'en';
    return window.localStorage.getItem('lang') || 'en';
  });

  const isRTL = lang === 'ar';

  useEffect(() => {
    if (typeof document === 'undefined') return;

    document.documentElement.lang = lang;
    document.documentElement.dir = isRTL ? 'rtl' : 'ltr';
    document.body.dir = isRTL ? 'rtl' : 'ltr';
    document.body.classList.toggle('rtl-mode', isRTL);
  }, [lang, isRTL]);

  const setLanguage = useCallback((nextLang) => {
    setLang(nextLang);
    if (typeof window !== 'undefined') {
      window.localStorage.setItem('lang', nextLang);
    }
  }, []);

  const toggleLang = useCallback(() => {
    setLanguage(lang === 'en' ? 'ar' : 'en');
  }, [lang, setLanguage]);

  const value = useMemo(
    () => ({
      lang,
      isRTL,
      t: (key, params) => {
        const message = TRANSLATIONS[lang]?.[key] ?? TRANSLATIONS.en[key] ?? key;
        return formatMessage(message, params);
      },
      setLanguage,
      toggleLang,
    }),
    [isRTL, lang, setLanguage, toggleLang]
  );

  return <LanguageContext.Provider value={value}>{children}</LanguageContext.Provider>;
}

export function useLanguage() {
  return useContext(LanguageContext);
}
